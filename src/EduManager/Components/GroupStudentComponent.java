		// TODO add your handling code here:

		Enrollment enrollment = new Enrollment();
		enrollment.setStudentId(studentId);
		enrollment.setGroupSubjectId(groupSubjectId);

		if (enrollmentController.addEnrollment(enrollment)) {
			// Obtener el panel padre y llamar al m√©todo fillPanelSchedule
			Component parent = this.getParent();
			while (parent != null && !(parent instanceof StudentForm)) {
				parent = parent.getParent();
			}

			if (parent != null) {
				((StudentForm) parent).fillPanelSchedule();
			}
		}
